# 1) დაწერეთ პროგრამა, რომელიც გამოიტანს ყველა კენტ რიცხვს 10-დან 45-ის ჩათვლით for loop-ის გამოყენებით.
for i in range(9,45,2):
    print(i)

# 2) დაწერეთ პროგრამა, რომელიც გამოიტანს ყველა ლუწ რიცხვს 20-დან 50-მდე while loop-ის გამოყენებით.
number = 20
while number <= 50:
    if number % 2 == 0:
        print(number)
    number += 1